package com.group64;

public class D2D {
    
    private int x;
    private int y;

    public D2D() {
        x = 0;
        y = 0;
    }

    public D2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int X) {
        this.x = X;
    }

    public int getY() {
        return y;
    }

    public void setY(int Y) {
        this.x = Y;
    }

    public int getWidth() {
        return x;
    }

    public int getHeight() {
        return y;
    }
}
